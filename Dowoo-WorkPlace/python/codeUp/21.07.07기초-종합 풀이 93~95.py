#문제풀이 1 : a배열을 만들고 그 안에 배열을 만들어서 0으로 가득채운다
#ex) a [[0,0,0],[0,0,0]] 이런식으로 채운다.

a = []
for i in range(20) :
    a.append([])
    for j in range(20) : 
        a[i].append(0)


#문제풀이 2 : 몇개를 둘것인지 n개를 받고 다음줄부터 x,y를 받아서 거기에 +1을 해줌.
n = int(input())

for i in range(n) : 
    x,y = input().split()
    a[int(x)][int(y)] = 1

#문제풀이 3 : 0번은 버리고 리스트를 프린트 하자!
