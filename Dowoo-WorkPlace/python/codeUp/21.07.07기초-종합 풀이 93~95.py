# 출석 번호를 n번 무작위로 불렀을 때, 가장 빠른 번호를 출력해 보자.

# 입력 예시   
# 10
# 10 4 2 3 6 6 7 9 8 5

# 출력 예시
# 2

#최소값 구하는 식

n = int(input())  
k = input().split()

for i in range(n) :
    k[i] = int(k[i]) 

min = k[0]

for i in range(1,n) :
    if min >= k[i] :
        min = k[i]

print(min)

